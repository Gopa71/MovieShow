# 10:Create a Python program that asks the user for the current weather conditions (e.g., "sunny," "rainy," "cloudy") and provides a message based on the weather using elif statements. For example:

# If it's "sunny," print "Don't forget your sunscreen!"
# If it's "rainy," print "Remember to bring an umbrella."
# If it's "cloudy," print "It might be a bit cool today."
a=str(input(" Enter the current weather conditions : "))
if a=="Sunny":
    print("Don't forget your sunscreen!")
elif a=="rainy":
    print("Remember to bring an umbrella.")
elif a=="cloudy":
    print("It might be a bit cool today.")